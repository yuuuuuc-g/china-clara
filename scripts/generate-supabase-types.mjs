#!/usr/bin/env node

import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { spawn } from "node:child_process";

const DEFAULT_OUTPUT = "src/lib/database.types.ts";
const DEFAULT_SCHEMA = "public";

function printHelp() {
  console.log(`Generate Supabase TypeScript types.

Usage:
  npm run supabase:types
  npm run supabase:types:local
  node scripts/generate-supabase-types.mjs --project-id <ref>
  node scripts/generate-supabase-types.mjs --db-url <postgres-url>

Options:
  --project-id <ref>  Remote Supabase project ref. Also reads SUPABASE_PROJECT_ID or SUPABASE_PROJECT_REF.
  --db-url <url>      Postgres connection URL. Also reads SUPABASE_DB_URL.
  --local             Generate from the local Supabase stack.
  --schema <name>     Schema to generate. Defaults to public.
  --output <path>     Output file. Defaults to ${DEFAULT_OUTPUT}.
  --help              Show this message.

Environment:
  SUPABASE_CLI_BIN    Supabase CLI executable. Defaults to "supabase".
`);
}

function readOption(args, name) {
  const index = args.indexOf(name);
  if (index === -1) {
    return undefined;
  }

  const value = args[index + 1];
  if (!value || value.startsWith("--")) {
    throw new Error(`${name} requires a value.`);
  }

  return value;
}

function buildSupabaseArgs(args) {
  const schema = readOption(args, "--schema") ?? DEFAULT_SCHEMA;
  const projectId =
    readOption(args, "--project-id") ??
    process.env.SUPABASE_PROJECT_ID ??
    process.env.SUPABASE_PROJECT_REF;
  const dbUrl = readOption(args, "--db-url") ?? process.env.SUPABASE_DB_URL;
  const useLocal = args.includes("--local");
  const generationArgs = ["gen", "types", "typescript", "--schema", schema];

  if (useLocal) {
    generationArgs.push("--local");
    return generationArgs;
  }

  if (dbUrl) {
    generationArgs.push("--db-url", dbUrl);
    return generationArgs;
  }

  if (projectId) {
    generationArgs.push("--project-id", projectId);
    return generationArgs;
  }

  throw new Error(
    "Missing Supabase target. Set SUPABASE_PROJECT_ID, SUPABASE_PROJECT_REF, SUPABASE_DB_URL, or pass --local."
  );
}

function runSupabase(cli, args) {
  return new Promise((resolveProcess, rejectProcess) => {
    const child = spawn(cli, args, {
      cwd: process.cwd(),
      env: process.env,
      stdio: ["ignore", "pipe", "inherit"],
    });

    let stdout = "";
    child.stdout.setEncoding("utf8");
    child.stdout.on("data", (chunk) => {
      stdout += chunk;
    });
    child.on("error", rejectProcess);
    child.on("close", (code) => {
      if (code === 0) {
        resolveProcess(stdout);
        return;
      }

      rejectProcess(new Error(`${cli} ${args.join(" ")} exited with code ${code}.`));
    });
  });
}

async function main() {
  const args = process.argv.slice(2);
  if (args.includes("--help") || args.includes("-h")) {
    printHelp();
    return;
  }

  const outputPath = resolve(readOption(args, "--output") ?? DEFAULT_OUTPUT);
  const supabaseArgs = buildSupabaseArgs(args);
  const cli = process.env.SUPABASE_CLI_BIN ?? "supabase";
  const generated = await runSupabase(cli, supabaseArgs);

  if (!/export\s+(interface|type)\s+Database\b/.test(generated)) {
    throw new Error("Supabase CLI output did not include a Database type.");
  }

  const header = `// This file is generated by npm run supabase:types.
// Do not edit it by hand. Update Supabase migrations/schema, then regenerate.

`;

  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, `${header}${generated.trimStart()}`);
  console.log(`Generated ${outputPath}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
